public class SelectSort {
    public int[] selectSort(int[] nums){
        int n = nums.length;
        for (int i = 0; i < n; i++) {
            int min = i;
            for (int j = i+1; j < n; j++) {
                if (nums[j]<nums[min]){
                    min = j;
                }

            }
            swap(nums,min,i);
        }
        return nums;
    }
    private static  void  swap(int[] nums,int i,int j){
        int temp = nums[i];
        nums[i]=nums[j];
        nums[j] = temp;
    }
}
